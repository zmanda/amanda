package Amanda::DB;

package Amanda::DB::Message;
use strict;
use warnings;

use Amanda::Message;
use vars qw( @ISA );
@ISA = qw( Amanda::Message );

sub local_message {
    my $self = shift;

    if ($self->{'code'} == 2600000) {
	return "The dumps";
    } elsif ($self->{'code'} == 2600001) {
	return "Volumes list";
    }
}

